using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace NachoOperaSyR
{
    public class Class1
    {
        /// <summary>
        /// Método Sumar
        /// </summary>
        /// <param name="m"></param>
        /// <param name="n"></param>
        /// <returns></returns>
        public static long Suma(long m, long n)
        {
            return (m + n);
        }

        /// <summary>
        /// Método Restar
        /// </summary>
        /// <param name="m"></param>
        /// <param name="n"></param>
        /// <returns></returns>
        public static long Resta(long m, long n)
        {
            return (m - n);
        }
    }
}

