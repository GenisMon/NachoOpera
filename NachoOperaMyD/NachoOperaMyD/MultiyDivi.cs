using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace NachoOperaMyD
{
    public class MultiyDivi
    {
        /// <summary>
        /// Metodo Multiplicar 
        /// </summary>
        /// <param name="m"></param>
        /// <param name="n"></param>
        /// <returns></returns>
        public static long Multiplicacion(long m, long n)
        {
            return (m * n);
        }

        /// <summary>
        /// Metodo Dividir
        /// </summary>
        /// <param name="m"></param>
        /// <param name="n"></param>
        /// <returns></returns>
        public static long Division(long m, long n)
        {
            return (m / n);
        }
    }
}
